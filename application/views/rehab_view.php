                    <h2>Реабилитационное занятие состоит из следующих процедур:</h2>
                        <p><strong>Термо-криоконтрастная терапия</strong> - <strong>лечение холодом и теплом</strong> основано на реакции организма на кратковременное локальное охлаждение и нагревание, оказывающие возбуждающий эффект на нервные рецепторы. В результате в них активизируются нервные процессы, снимается мышечный тонус, улучшается метаболизм тканей, происходит рассасывание спаек и рубцов стимулируется иммунная система, происходит обезболивание.</p>
                        <br/>
                        <p><strong>ПНФ (Проприоцептивное нейромышечное облегчение (PNF) и лечебная физическая культура (ЛФК) </strong> в основе метода <strong>лечебной физической культуры</strong> лежат активные и пассивные двигательные упражнения. С их помощью восстанавливаются функции опорно-двигательного аппарата у больного, формируются правильные двигательные стереотипы и положение тела. Широко применяемый метод, как при лечении различных заболеваний, так и с профилактическими целями.</p>
                        <br/>
                        <p><strong>Кинезитерапия </strong> -  лечение физическими упражнениями с помощью специальных аппаратов.</p>
                        <br/>
                        <p><strong>Эрготерапия </strong>(ergon – труд) это метод лечения, основанный на выполнении пациентом определенных двигательных заданий – самостоятельно либо с помощью специальных тренажеров, персонала. Цели эрготерапии – помочь пациентам восстановить утраченные функции самообслуживания, заново научиться выполнять обычные повседневные дела, восстановить трудовые навыки.</p>
                        <br/>
                        <p><strong>Арт-терапия</strong> - процесс творчества стимулирует эмоциональную и психическую гармонизацию. Нервная система на занятиях арт-терапией успокаивается, реализуется творческий потенциал пациента, его самовыражение, происходит развитие мелкой моторики рук, пациент учится концентрации внимания. Так же занятия арт-терапией косвенно стимулируют снижение спастики мышц.</p>
                        <br/>
                        <p><strong>Лечебный массаж</strong> - терапевтический массаж – это специализированный массаж, направленный на достижение определенного эффекта. Соответственно применяются определенные массажные техники, позволяющие стимулировать или нормализовать нарушенные в организме функции.</p>

